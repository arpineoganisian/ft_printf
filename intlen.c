#include "libftprintf.h"

int	intlen(long n, t_struct *l)
{
	int len;

	len = 0;
	if (n == 0 && l->precision != 0)
		len = 1;
	while (n > 0)
	{
		n = n / 10;
		len++;
	}
	return (len);
}
